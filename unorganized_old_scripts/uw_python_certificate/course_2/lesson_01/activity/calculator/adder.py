"""This module provides a addition operator."""


class Adder:
    """A class for adding operands."""
    @staticmethod
    def calc(operand_1, operand_2):
        """Adds the numbers.
        Args:
            operand_1: first number to add
            operand_2: second number to add
        Returns:
            both numbers added together
        """
        return operand_1 + operand_2
